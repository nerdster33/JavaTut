package learningjava;

/**
 * short data type ranges from -2^15 to 2^15-1
 * Created by Nerd on 6/16/2017.
 */
public class ShortDataType {


    public static void main(String[] args) {
        // from upper to lower - downcasting
        // from lower to upper - upcasting
        short a = -32768;
        System.out.println("The value of a is "+a);

    }
}
